package edu.ucsur.munayi.model;

public class Dolencias{
	private String nombreDolencia;

	public Dolencias() {
		super();
	}

	public Dolencias(String nombreDolencia) {
		super();
		this.nombreDolencia = nombreDolencia;
	}

	public String getNombreDolencia() {
		return nombreDolencia;
	}

	public void setNombreDolencia(String nombreDolencia) {
		this.nombreDolencia = nombreDolencia;
	}
	
}
